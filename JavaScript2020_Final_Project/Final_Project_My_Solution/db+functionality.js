let category
let res
let curIdx
let score
let gameDisplay
let btn
let updatedScore
let wordInGame
let imgInGame
let optionsArr
let categoryName
let firstTry
let theRightAnswer
let correctAnswerSvg
let incorrectAnswerSvg

function setCategory(categoryId) {
    switch (categoryId) {
        case "Musical Instruments":
            category = musicalInstruments
            categoryName = "Musical Instruments" 
            break;
        case "Animals":
            category = animals
            categoryName = "Animals"
            break;
        case "Drinks":
            category = drinks
            categoryName = "Drinks"
            break;
        case "Plants":
            category = plants
            categoryName = "Plants"
            break;
        case "Clothes":
            category = clothes
            categoryName = "Clothes"
            break;
    }
    loadGame()
}

function loadGame() {
    curIdx = 0
    score = 0
    firstTry = true
    updatedScore = document.getElementById("gamePageScorePoints")
    updatedScore.innerHTML =score
    gameDisplay = [document.getElementById("gamePageMiddleDiv"),                            document.getElementById("gamePageChooseMessage") ,document.getElementById("gamePageBottom")]
    wordInGame = document.getElementById("word")
    imgInGame = document.getElementById("img")
    optionsArr = [document.getElementById("option1"), document.getElementById("option2"),   document.getElementById("option3")]
    wordInGame.innerHTML = category[0].word 
    imgInGame.src = category[0].img
    theRightAnswer = category[0].rightAnswer
    correctAnswerSvg = document.getElementById("correctAnswerSvg")
    incorrectAnswerSvg = document.getElementById("incorrectAnswerSvg")

    for (let optionIdx = 0; optionIdx < optionsArr.length; optionIdx++) {
        optionsArr[optionIdx].innerHTML = category[0].options[optionIdx] 
    }
    gameDisplay[0].style.display="block"
    gameDisplay[1].style.display="none"
}

function runGame(btnid) {
    btn = document.getElementById(btnid)
    updatedScore = document.getElementById("gamePageScorePoints")
    res = document.getElementById("isAnswerCorrect")
    updateGame(btn, updatedScore, res)

}

function updateGame(btn, updatedScore, res) {
    gameDisplay[2].style.display="block"
    if (btn.innerHTML != "previous" && btn.innerHTML != "next") {
        if (btn.innerHTML == theRightAnswer) {
            if (firstTry == true) {
                score = 1
            }
            else {
                score = 0
            }
            res.innerHTML = "Correct!"
            res.style.display = "inline"
            res.style.color = "#33FF36"
            correctAnswerSvg.style.display = "inline"
            incorrectAnswerSvg.style.display = "none" 
        }
        else {
            score = - 0.5
            res.innerHTML = "Wrong answer, please try again!"
            res.style.display = "inline"
            res.style.color = "#FF3333"
            correctAnswerSvg.style.display = "none"
            incorrectAnswerSvg.style.display = "inline" 
        }
        gameDisplay[2].style.display="block"
        firstTry = false
        updatedScore.innerHTML = Number(updatedScore.innerHTML) + score
    }
    else {
        gameDisplay[2].style.display="none" 
        correctAnswerSvg.style.display = "none"
        incorrectAnswerSvg.style.display = "none" 
    }
    moveQuestions(btn)
} 

function moveQuestions(btn) {
    if (btn.innerHTML == "previous") {
        if (curIdx > 0) {
            curIdx--
            firstTry = true
        }
    }
    else if (btn.innerHTML == "next" || btn.innerHTML == theRightAnswer) {
        if (curIdx < category.length) {
            curIdx++
            firstTry = true
        }
    }
    
    if (btn.innerHTML == "previous" || btn.innerHTML == "next") {
        continueGame()
    }
    else {
        setTimeout(continueGame, 1000)
    }  
} 

function continueGame() {
    res.innerHTML=""
    gameDisplay[2].style.display="none"
    if (curIdx == category.length) {
        gameDisplay[0].style.display="none"
        gameDisplay[1].style.display="block"
        let messageLine01 = document.getElementById("theMessageLine01")
        messageLine01.innerHTML = `This is the end of the category ${categoryName}!`
        let messageLine02 = document.getElementById("theMessageLine02")
        messageLine02.innerHTML = `Your Final Score = ${updatedScore.innerHTML}`
        let messageLine03 = document.getElementById("theMessageLine03")
        messageLine03.innerHTML = "Would you like to play again?"
        let messageLine04 = document.getElementById("theMessageLine04")
        messageLine04.innerHTML = "if yes, choose the same category, else choose another one!"


        updatedScore.innerHTML = 0
    }
    else {
        wordInGame.innerHTML = category[curIdx].word 
        imgInGame.src=category[curIdx].img
        theRightAnswer = category[curIdx].rightAnswer
        for (let optionIdx = 0; optionIdx < optionsArr.length; optionIdx++) {
            optionsArr[optionIdx].innerHTML = category[curIdx].options[optionIdx] 
        }

    }
}



